export interface MockQuestion {
  id: string
  questionType: 'MCQ' | 'CODING' | 'DEBUGGING' | 'SQL'
  questionData: any
  difficulty: 'EASY' | 'MEDIUM' | 'HARD'
  topic: string
  marks: number
  orderIndex: number
  companyTags?: string[]
}

export interface MockAssessment {
  id: string
  title: string
  description: string
  type: string
  duration: number
  totalQuestions: number
  totalMarks: number
  passingMarks: number
  questions: MockQuestion[]
  negativeMarking: number
  questionPoolSize: number
  randomizeOnRetake: boolean
  companyPattern?: string
}

function mcq(
  id: string, text: string, options: { label: string; value: string }[],
  correctAnswer: string, explanation: string, difficulty: MockQuestion['difficulty'],
  topic: string, marks = 1, companyTags?: string[]
): MockQuestion {
  return { id, questionType: 'MCQ', difficulty, topic, marks, orderIndex: 0, companyTags, questionData: { text, options, correctAnswer, explanation } }
}

function coding(
  id: string, title: string, description: string, constraints: string,
  sampleInput: string, sampleOutput: string,
  testCases: { input: string; expectedOutput: string }[],
  hiddenTestCases: { input: string; expectedOutput: string }[],
  starterCode: Record<string, string>,
  difficulty: MockQuestion['difficulty'], topic: string, marks: number,
  explanation?: string, companyTags?: string[]
): MockQuestion {
  return { id, questionType: 'CODING', difficulty, topic, marks, orderIndex: 0, companyTags, questionData: { title, description, constraints, sampleInput, sampleOutput, testCases, hiddenTestCases, starterCode, explanation } }
}

