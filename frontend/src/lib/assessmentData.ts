function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array]
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]]
  }
  return shuffled
}

function balanceDifficulty(questions: MockQuestion[], count: number): MockQuestion[] {
  const easy = questions.filter(q => q.difficulty === 'EASY')
  const medium = questions.filter(q => q.difficulty === 'MEDIUM')
  const hard = questions.filter(q => q.difficulty === 'HARD')

  const easyCount = Math.floor(count * 0.4)
  const mediumCount = Math.floor(count * 0.4)
  const hardCount = count - easyCount - mediumCount

  const selected = [
    ...shuffleArray(easy).slice(0, easyCount),
    ...shuffleArray(medium).slice(0, mediumCount),
    ...shuffleArray(hard).slice(0, hardCount),
  ]

  return shuffleArray(selected).map((q, i) => ({ ...q, orderIndex: i }))
}

export function getRandomizedAssessment(
  assessmentId: string,
  previousAttemptQuestionIds?: string[]
): MockAssessment | null {
  const assessment = MOCK_ASSESSMENTS.find(a => a.id === assessmentId)
  if (!assessment) return null

  if (!assessment.randomizeOnRetake) return assessment

  let pool = [...assessment.questions]

  if (previousAttemptQuestionIds && previousAttemptQuestionIds.length > 0) {
    const unseen = pool.filter(q => !previousAttemptQuestionIds.includes(q.id))
    if (unseen.length >= assessment.totalQuestions) {
      pool = unseen
    }
  }

  const balanced = balanceDifficulty(pool, assessment.totalQuestions)
  const totalMarks = balanced.reduce((sum, q) => sum + q.marks, 0)

  return {
    ...assessment,
    questions: balanced,
    totalMarks,
    passingMarks: Math.ceil(totalMarks * 0.4),
  }
}

